// 题目核心限制：不能修改数组 + 只能用 O (1) 额外空间
// 快慢指针，时间复杂度 O (n)，空间复杂度 O (1)
public class Solution {
    public int findDuplicate(int[] nums) {
        // 1. 快慢指针找相遇点
        int slow = 0, fast = 0;
        do {
            slow = nums[slow];       // 慢指针走1步
            fast = nums[nums[fast]]; // 快指针走2步
        } while (slow != fast);

        // 2. 慢指针回起点，同速走找环入口
        slow = 0;
        while (slow != fast) {
            slow = nums[slow];
            fast = nums[fast];
        }
        return slow; // 相遇点就是重复数
    }
}
/*
1. 问题转化原理
数组长度为 n+1，元素范围 [1, n]，我们可以把数组看成一个链表映射：
数组下标 i 对应链表的「节点」
nums[i] 对应「节点 i 的下一个节点」
因为数组中存在重复数，所以这个链表必然存在环，而环的入口点就是重复的数。
例：nums = [1,3,4,2,2]映射关系：0→1→3→2→4→2→4... 环的入口是 2，也就是重复数
2. Floyd 快慢指针步骤
找相遇点：快慢指针从起点 0 出发，快指针每次走 2 步，慢指针每次走 1 步，直到两者相遇
找环入口（重复数）：慢指针回到起点 0，快慢指针都每次走 1 步，再次相遇的点就是环入口，即重复数
 */

